# ══════════════════════════════════════════════════════════════════════
#  PART 1 – PrunableLinear Layer
# ══════════════════════════════════════════════════════════════════════

class PrunableLinear(torch_nn.Module):
    """
    A drop-in replacement for nn.Linear that attaches a learnable scalar
    gate to every weight element.

    Forward pass:
        gates        = sigmoid(gate_scores)          ∈ (0, 1)
        pruned_weight = weight * gates               (element-wise)
        output        = pruned_weight @ x^T + bias

    Because sigmoid is differentiable everywhere, gradients flow cleanly
    through both `weight` and `gate_scores` during back-propagation.
    """

    def __init__(self, in_features: int, out_features: int):
        super().__init__()
        self.in_features  = in_features
        self.out_features = out_features

        # ── Standard parameters ──────────────────────────────────────
        self.weight = torch_nn.Parameter(torch.Tensor(out_features, in_features))
        self.bias   = torch_nn.Parameter(torch.Tensor(out_features))

        # ── Gate scores (same shape as weight) ───────────────────────
        # Registered as a Parameter so the optimiser updates them too.
        # Initialised with small normal noise → sigmoid ≈ 0.5 at start.
        self.gate_scores = torch_nn.Parameter(torch.Tensor(out_features, in_features))

        self._reset_parameters()

    # ------------------------------------------------------------------
    def _reset_parameters(self):
        # Standard Kaiming uniform init for the weight matrix
        torch_nn.init.kaiming_uniform_(self.weight, a=math.sqrt(5))

        # Bias init matching nn.Linear's default
        fan_in, _ = torch_nn.init._calculate_fan_in_and_fan_out(self.weight)
        bound = 1 / math.sqrt(fan_in) if fan_in > 0 else 0
        torch_nn.init.uniform_(self.bias, -bound, bound)

        # Gate scores: small normal → sigmoid(score) ≈ 0.5 initially
        torch_nn.init.normal_(self.gate_scores, mean=0.0, std=0.1)

    # ------------------------------------------------------------------
    def forward(self, x: torch.Tensor) -> torch.Tensor:
        # Step 1 – convert raw scores to gates in (0, 1)
        gates = torch.sigmoid(self.gate_scores)

        # Step 2 – element-wise gate the weights
        pruned_weights = self.weight * gates

        # Step 3 – standard affine transform
        return F.linear(x, pruned_weights, self.bias)

    # ------------------------------------------------------------------
    def sparsity_info(self, threshold: float = 1e-2):
        """Return (n_pruned, n_total) for this layer."""
        with torch.no_grad():
            gates    = torch.sigmoid(self.gate_scores)
            n_pruned = int((gates < threshold).sum().item())
        return n_pruned, self.gate_scores.numel()


# ══════════════════════════════════════════════════════════════════════
#  PART 1 (cont.) – The Network
# ══════════════════════════════════════════════════════════════════════

class SelfPruningMLP(torch_nn.Module):
    """
    Three-layer MLP for CIFAR-10.
    Input: 3 × 32 × 32 = 3 072 features   Output: 10 classes
    All linear layers use PrunableLinear.
    """

    def __init__(self):
        super().__init__()
        self.flatten = torch_nn.Flatten()
        self.fc1 = PrunableLinear(3072, 512)
        self.fc2 = PrunableLinear(512,  256)
        self.fc3 = PrunableLinear(256,   10)

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        x = self.flatten(x)
        x = F.relu(self.fc1(x))
        x = F.relu(self.fc2(x))
        return self.fc3(x)

    def sparsity_level(self, threshold: float = 1e-2) -> float:
        """Overall % of weight-gates that are below `threshold`."""
        total_pruned = total_weights = 0
        for m in self.modules():
            if isinstance(m, PrunableLinear):
                p, t = m.sparsity_info(threshold)
                total_pruned  += p
                total_weights += t
        return 100.0 * total_pruned / total_weights if total_weights else 0.0


# ══════════════════════════════════════════════════════════════════════
#  PART 2 – Sparsity Regularisation Loss
# ══════════════════════════════════════════════════════════════════════

def calculate_sparsity_loss(model: SelfPruningMLP) -> torch.Tensor:
    """
    L1 penalty on all gate values.

    SparsityLoss = Σ_layers Σ_i sigmoid(gate_scores_i)

    Why L1 encourages sparsity
    ──────────────────────────
    The L1 norm penalises every gate proportionally to its value.
    Unlike L2, whose gradient vanishes near zero, L1 applies a constant
    sub-gradient of ±1 regardless of magnitude.  This constant "pull
    toward zero" is strong enough to drive small, unimportant gates all
    the way to zero, while large gates (that carry useful signal) resist
    the penalty because the classification loss still rewards them.
    The result is a bimodal distribution: many gates collapse to ≈ 0
    (pruned) and a few remain large (kept).
    """
    sparsity_loss = torch.tensor(0.0, device=next(model.parameters()).device)
    for m in model.modules():
        if isinstance(m, PrunableLinear):
            # Gates are always positive, so L1 norm = sum of gate values
            sparsity_loss = sparsity_loss + torch.sum(torch.sigmoid(m.gate_scores))
    return sparsity_loss


# ══════════════════════════════════════════════════════════════════════
#  PART 3 – Training & Evaluation
# ══════════════════════════════════════════════════════════════════════

def train_and_eval(
    lambda_val: float,
    trainloader,
    testloader,
    device: torch.device,
    epochs: int = 10,
) -> tuple:
    """
    Train one SelfPruningMLP with the given λ, then evaluate it.

    Total Loss = CrossEntropyLoss + λ × SparsityLoss

    Returns
    -------
    model      : trained SelfPruningMLP
    accuracy   : float  – test accuracy (%)
    sparsity   : float  – % of gates < 1e-2 after training
    """
    print(f"\n{'━'*58}")
    print(f"  λ = {lambda_val}  |  epochs = {epochs}")
    print(f"{'━'*58}")

    model     = SelfPruningMLP().to(device)
    criterion = torch_nn.CrossEntropyLoss()
    optimizer = optim.Adam(model.parameters(), lr=1e-3)

    # ── Training loop ──────────────────────────────────────────────
    for epoch in range(epochs):
        model.train()
        running_loss = 0.0

        for i, (inputs, labels) in enumerate(trainloader):
            inputs, labels = inputs.to(device), labels.to(device)

            optimizer.zero_grad()

            outputs = model(inputs)

            # Classification loss
            cls_loss  = criterion(outputs, labels)

            # Sparsity regularisation (L1 on all gates)
            spar_loss = calculate_sparsity_loss(model)

            # Combined loss
            loss = cls_loss + lambda_val * spar_loss

            loss.backward()      # gradients flow through weight AND gate_scores
            optimizer.step()

            running_loss += loss.item()
            if i % 200 == 199:
                print(
                    f"  epoch {epoch+1:>2}/{epochs}  "
                    f"step {i+1:>5d}  "
                    f"loss {running_loss / 200:.4f}  "
                    f"sparsity {model.sparsity_level():.1f}%"
                )
                running_loss = 0.0

    # ── Evaluation ─────────────────────────────────────────────────
    model.eval()
    correct = total = 0
    with torch.no_grad():
        for inputs, labels in testloader:
            inputs, labels = inputs.to(device), labels.to(device)
            _, predicted   = torch.max(model(inputs), 1)
            total   += labels.size(0)
            correct += (predicted == labels).sum().item()

    accuracy = 100.0 * correct / total
    sparsity = model.sparsity_level(threshold=1e-2)

    print(f"\n  ✓  Test Accuracy  : {accuracy:.2f}%")
    print(f"  ✓  Sparsity Level : {sparsity:.2f}%  (gates < 1e-2)")

    return model, accuracy, sparsity


