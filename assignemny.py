def main():
    device = torch.device("cuda:0" if torch.cuda.is_available() else "cpu")
    print(f"Device: {device}\n")

    # ── CIFAR-10 data loaders ────────────────────────────────────────
    transform = transforms.Compose([
        transforms.ToTensor(),
        transforms.Normalize((0.5, 0.5, 0.5), (0.5, 0.5, 0.5)),
    ])

    trainset = torchvision.datasets.CIFAR10(
        root="./data", train=True,  download=True, transform=transform)
    testset  = torchvision.datasets.CIFAR10(
        root="./data", train=False, download=True, transform=transform)

    trainloader = torch.utils.data.DataLoader(
        trainset, batch_size=64, shuffle=True,  num_workers=2)
    testloader  = torch.utils.data.DataLoader(
        testset,  batch_size=64, shuffle=False, num_workers=2)

    # ── Lambda sweep (low / medium / high) ──────────────────────────
    # λ=0        → no pruning pressure  (baseline)
    # λ=1e-5     → gentle pruning
    # λ=1e-4     → medium pruning
    # λ=1e-3     → strong pruning
    #
    # NOTE: We use the raw L1 sum as required by the assignment spec.
    # With ~1.7 M gates the sparsity term is already large, so even
    # λ=1e-5 applies meaningful pressure.
    lambda_values = [0.0, 1e-5, 1e-4, 1e-3]

    results    = []
    best_model = None          # we'll plot the gate dist for λ = 1e-4

    for lv in lambda_values:
        model, acc, spar = train_and_eval(
            lv, trainloader, testloader, device, epochs=10
        )
        results.append({"lambda": lv, "accuracy": acc, "sparsity": spar})
        if lv == 1e-4:
            best_model = model

    # ── Summary table ────────────────────────────────────────────────
    print("\n" + "═" * 52)
    print(f"  {'Lambda':<12} {'Test Acc (%)':>14} {'Sparsity (%)':>14}")
    print("═" * 52)
    for r in results:
        print(f"  {r['lambda']:<12.0e} {r['accuracy']:>14.2f} {r['sparsity']:>14.2f}")
    print("═" * 52)

    # ── Plots ────────────────────────────────────────────────────────
    print("\nSaving plots …")
    if best_model is not None:
        plot_gate_distribution(best_model, lambda_val=1e-4,
                               path="gate_distribution.png")
    plot_accuracy_sparsity(results, path="accuracy_vs_sparsity.png")
    print("\nAll done.")


if __name__ == "__main__":
    multiprocessing.freeze_support()
    main()

