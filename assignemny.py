def get_all_gates(model: SelfPruningMLP) -> list:
    """Collect every gate value (as a Python float) across all layers."""
    gates = []
    with torch.no_grad():
        for m in model.modules():
            if isinstance(m, PrunableLinear):
                gates.extend(
                    torch.sigmoid(m.gate_scores).flatten().cpu().tolist()
                )
    return gates


def plot_gate_distribution(model: SelfPruningMLP, lambda_val: float,
                           path: str = "gate_distribution.png") -> None:
    """
    Histogram of all gate values for the given model.
    A successful pruning run shows a large spike near 0 and a smaller
    cluster of surviving gates clustered away from 0.
    """
    gates = get_all_gates(model)

    fig, ax = plt.subplots(figsize=(9, 5))
    ax.hist(gates, bins=80, color="#3B82F6", edgecolor="white",
            linewidth=0.3, alpha=0.88)
    ax.set_yscale("log")                # log-scale reveals the non-zero tail
    ax.set_xlim(0, 1)
    ax.set_title(
        f"Distribution of Gate Values   (λ = {lambda_val})\n"
        f"Sparsity: {model.sparsity_level():.1f}%  |  "
        f"Total gates: {len(gates):,}",
        fontsize=12
    )
    ax.set_xlabel("Gate Value  [sigmoid output]", fontsize=11)
    ax.set_ylabel("Frequency  (log scale)",       fontsize=11)
    ax.axvline(0.01, color="red", linestyle="--", linewidth=1.2,
               label="Pruning threshold (0.01)")
    ax.legend(fontsize=10)
    ax.grid(True, alpha=0.25)
    plt.tight_layout()
    plt.savefig(path, dpi=150)
    plt.close()
    print(f"  Saved → '{path}'")


def plot_accuracy_sparsity(results: list,
                           path: str = "accuracy_vs_sparsity.png") -> None:
    """Dual-axis bar chart: accuracy and sparsity vs λ."""
    labels     = [f"λ={r['lambda']}" for r in results]
    accuracies = [r["accuracy"] for r in results]
    sparsities = [r["sparsity"] for r in results]

    x   = list(range(len(labels)))
    fig, ax1 = plt.subplots(figsize=(9, 5))

    ax1.bar([i - 0.2 for i in x], accuracies, width=0.38,
            label="Test Accuracy (%)", color="#10B981", alpha=0.85)
    ax1.set_ylabel("Test Accuracy (%)", color="#10B981", fontsize=11)
    ax1.tick_params(axis="y", labelcolor="#10B981")
    ax1.set_ylim(0, 100)

    ax2 = ax1.twinx()
    ax2.bar([i + 0.2 for i in x], sparsities, width=0.38,
            label="Sparsity (%)", color="#F59E0B", alpha=0.85)
    ax2.set_ylabel("Sparsity (%)", color="#F59E0B", fontsize=11)
    ax2.tick_params(axis="y", labelcolor="#F59E0B")
    ax2.set_ylim(0, 100)

    ax1.set_xticks(x)
    ax1.set_xticklabels(labels, fontsize=11)
    ax1.set_title("Test Accuracy vs Sparsity Level across λ values", fontsize=12)

    h1, l1 = ax1.get_legend_handles_labels()
    h2, l2 = ax2.get_legend_handles_labels()
    ax1.legend(h1 + h2, l1 + l2, loc="upper left", fontsize=10)

    plt.tight_layout()
    plt.savefig(path, dpi=150)
    plt.close()
    print(f"  Saved → '{path}'")


# ══════════════════════════════════════════════════════════════════════
#  Main
